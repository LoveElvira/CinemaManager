public class PointsEntity {

    private String pointsId;
    private String pointsFlag;
    private String pointsNum;
    private String pointsContent;
    private String pointsTime;

    public String getPointsId() {
        return pointsId;
    }

    public void setPointsId(String pointsId) {
        this.pointsId = pointsId;
    }

    public String getPointsFlag() {
        return pointsFlag;
    }

    public void setPointsFlag(String pointsFlag) {
        this.pointsFlag = pointsFlag;
    }

    public String getPointsNum() {
        return pointsNum;
    }

    public void setPointsNum(String pointsNum) {
        this.pointsNum = pointsNum;
    }

    public String getPointsContent() {
        return pointsContent;
    }

    public void setPointsContent(String pointsContent) {
        this.pointsContent = pointsContent;
    }

    public String getPointsTime() {
        return pointsTime;
    }

    public void setPointsTime(String pointsTime) {
        this.pointsTime = pointsTime;
    }
}
